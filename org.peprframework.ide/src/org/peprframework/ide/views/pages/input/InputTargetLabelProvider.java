package org.peprframework.ide.views.pages.input;

import org.eclipse.jface.viewers.ITableLabelProvider;
import org.eclipse.jface.viewers.LabelProvider;
import org.eclipse.swt.graphics.Image;
import org.peprframework.ide.views.pages.input.InputTargetContentProvider.InputProperty;

/**
 * @author Volker Fritzsch
 * @version 1.0
 *
 */
public class InputTargetLabelProvider extends LabelProvider implements ITableLabelProvider {

	/* (non-Javadoc)
	 * @see org.eclipse.jface.viewers.ITableLabelProvider#getColumnImage(java.lang.Object, int)
	 */
	public Image getColumnImage(Object element, int columnIndex) {
		return null;
	}

	/* (non-Javadoc)
	 * @see org.eclipse.jface.viewers.LabelProvider#getText(java.lang.Object)
	 */
	public String getText(Object element) {
		InputProperty inputProperty = (InputProperty) element;
		return inputProperty.getType().getSimpleName() + " " + inputProperty.getName();
	}

	/* (non-Javadoc)
	 * @see org.eclipse.jface.viewers.ITableLabelProvider#getColumnText(java.lang.Object, int)
	 */
	public String getColumnText(Object element, int columnIndex) {
		switch (columnIndex) {
		case 0:
			return ((InputProperty) element).getType().getSimpleName();
		case 1:
			return ((InputProperty) element).getName();
		}

		return "UNKNOWN_COLUMN";
	}
	
}
